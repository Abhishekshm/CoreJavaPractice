//  declaring class
class LogicalDemo
{
	//calling main method
	public static final void main(String args[])
	{
		
		boolean b1= false, b2= true;
		
		//Logical Operators
		System.out.println("Logical OR output is : "+ (b1|b2));
		System.out.println("Logical AND output is : "+ (b1&b2));
		System.out.println(56<<4); //Left Shift
		System.out.println(67>>3); //Right Shift
		System.out.println(-20<<2);
		
		
	}
	//end of main
}//end of class LogicalDemo
